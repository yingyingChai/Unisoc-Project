using System;
namespace KaYi.FileSystem.Model
{
	public class FolderSummary
	{
		private string folderID = string.Empty;
		private string folderName = string.Empty;
		private int totalFiles;
		private ulong totalSize;
		private string simpleSizeDescription = string.Empty;
		private int filesInFolder;
		public string SimpleSizeDescription
		{
			get
			{
				return this.simpleSizeDescription;
			}
			set
			{
				this.simpleSizeDescription = value;
			}
		}
		public int FilesInFolder
		{
			get
			{
				return this.filesInFolder;
			}
			set
			{
				this.filesInFolder = value;
			}
		}
		public string FolderID
		{
			get
			{
				return this.folderID;
			}
			set
			{
				this.folderID = value;
			}
		}
		public string FolderName
		{
			get
			{
				return this.folderName;
			}
			set
			{
				this.folderName = value;
			}
		}
		public int TotalFiles
		{
			get
			{
				return this.totalFiles;
			}
			set
			{
				this.totalFiles = value;
			}
		}
		public ulong TotalSize
		{
			get
			{
				return this.totalSize;
			}
			set
			{
				this.totalSize = value;
			}
		}
	}
}
