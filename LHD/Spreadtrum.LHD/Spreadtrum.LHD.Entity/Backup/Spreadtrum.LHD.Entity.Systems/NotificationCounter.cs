            {
                this._LastWaitForConfirm = value;
            }
        }

        public int NewComments
        {
            get
            {
                return this._NewComments;
            }
            set
            {
                this._NewComments = value;
            }
        }

        public string UserID
        {
            get
            {
                return this._UserID;
            }
            set
            {
                this._UserID = value;
            }
        }

        public int WaitDispose
        {
            get
            {
                return this._WaitDispose;
            }
            set
            {
                this._WaitDispose = value;
            }
        }

        public int WaitForConfirm
        {
            get
            {
                return this._WaitForConfirm;
            }
            set
            {
                this._WaitForConfirm = value;
            }
        }

        public int WaitForOtherBinDispose
        {
            get
            {
                return this._WaitForOtherBinDispose;
            }
            set
            {
                this._WaitForOtherBinDispose = value;
            }
        }
    }
}

